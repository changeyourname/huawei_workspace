        bport1_ab_data.HRS.value = 0x0;
        bport1_ab_data.ERR.value = 0x0;
        bport1_ab_data.INT.value = 0x0;
        bport1_ab_data.EEI.value = 0x0;
        bport1_ab_data.ERQ.value = 0x0;
        bport1_ab_data.ES.value = 0x0;
        bport1_ab_data.CR.value = 0x400;
