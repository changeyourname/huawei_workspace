//////////////////////////////// Mask functions ////////////////////////////////

static PPM_WRITE_CB(bport1_ab_EARS_maskWrite) {
    bport1_ab_data.EARS.value = (bport1_ab_data.EARS.value & 0x0) | (data & 0xffffffff);
}

